package TernaryOperator;

public class SwitchStatement {
    public static void main(String[] args) {
        int number = 10;
        switch(number) {
            case 1: System.out.println("chocolate");
                    break;
            case 2: System.out.println("choco-chips");
                    break;
            case 3: System.out.println("milk-chocolate");
                    break;
            default:System.out.println("Never Give up");


        }

    }
}
